int ft_iterative_factorial(int nb) 
{
	int z;
	z = 1;

	if (nb<0) {
		return (0);
	}
	else if (nb>12) {
		return (0);
	}
	while (nb >= 1) {
		z= z*nb;
		nb -=1;
	}
	return (z);
}
